    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
    	TweetDataBase db = new TweetDataBase();
        List<Tweets> tweets = db.loadDBTweets();
    	SearchForTweets s = new SearchForTweets();
        var item = jitem.getText();
        int nbrTweets;
        nbrTweets = Integer.parseInt(numberOfTweet.getText());
        String strategy = choice2.getSelectedItem();
        s.getTweet(item,nbrTweets);
        ArrayList<Tweets> res;
        if(evt.getSource() != button){
        } else {
            Tresult.setText("");
            res=s.getResultsOfTweets();
            TweetDataBase a = new TweetDataBase();
            ArrayList<Tweets> loadDBTweets = a.loadDBTweets();
            
            for (int i=0 ;i<res.size();i++){
            
                switch (strategy) {
                    case "KeyWords":
                    {
                        KeyWords pol = new KeyWords(tweets,"databases/positive.txt","databases/negative.txt");
                        pol.classify(res.get(i));
                        //CountWord(res.get(i),"databases/negative.txt","databases/positive.txt");
                        Tresult.append(res.get(i).toString());
                        break;
                    }
                    case "aucune":
                    {
                        Tresult.append(res.get(i).toString());
                        
                        
                        //Tresult.append(loadDBTweets.get(i).toString());
                        break;
                    }
                    case "KNN":
                        {
                            
                            KNN knn = new KNN(loadDBTweets,5);
                            knn.classify(res.get(i));
                            //Tresult.append(database.toString());
                            Tresult.append(res.get(i).toString());
                            break;
                        }
                    case "naive Bayes Presence Unigramme":
                        {
                            
                            NaiveBayes nb = new NaiveBayes(loadDBTweets);
                            nb.classify(res.get(i), 1, 1);
                            Tresult.append(res.get(i).toString());
                            break;
                        }
                    case "naive Bayes Presence Bigramme":
                        {
                            //TweetDataBase a = new TweetDataBase();
                            //ArrayList<Tweets> database = a.loadDBTweets();
                            NaiveBayes nb = new NaiveBayes(loadDBTweets);
                            nb.classify(res.get(i), 1, 2);
                            Tresult.append(res.get(i).toString());
                            break;
                        }
                    case "naive Bayes Presence Unigramme + Bigramme":
                        {
                            //TweetDataBase a = new TweetDataBase();
                            //ArrayList<Tweets> database = a.loadDBTweets();
                            NaiveBayes nb = new NaiveBayes(loadDBTweets);
                            nb.classify(res.get(i), 1, 0);
                            Tresult.append(res.get(i).toString());
                            break;
                        }
                    case "naive Bayes frequence Unigramme":
                        {
                            //TweetDataBase a = new TweetDataBase();
                            //ArrayList<Tweets> database = a.loadDBTweets();
                            NaiveBayes nb = new NaiveBayes(loadDBTweets);
                            nb.classify(res.get(i), 0, 1);
                            Tresult.append(res.get(i).toString());
                            break;
                        }
                    case "naive Bayes frequence Bigramme":
                        {
                            //TweetDataBase a = new TweetDataBase();
                            //ArrayList<Tweets> database = a.loadDBTweets();
                            NaiveBayes nb = new NaiveBayes(loadDBTweets);
                            nb.classify(res.get(i), 0, 2);
                            Tresult.append(res.get(i).toString());
                            break;
                        }
                    case "naive Bayes frequence Unigramme + Bigramme":
                        {
                            //TweetDataBase a = new TweetDataBase();
                            //ArrayList<Tweets> database = a.loadDBTweets();
                            NaiveBayes nb = new NaiveBayes(loadDBTweets);
                            nb.classify(res.get(i), 0, 0);
                            Tresult.append(res.get(i).toString());
                            break;
                        }
                    default:
                        break;
                }
                
                
            }
        }
    }//GEN-LAST:event_buttonActionPerformed

